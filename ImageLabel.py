import tkinter as tk
from itertools import count, cycle
from PIL import Image, ImageTk

class ImageLabel(tk.Label):
    
    # Um label que exibe imagens e as reproduz se forem gifs, :im:, uma inst√¢ncia de imagem PIL ou um nome de arquivo de string
    
    def load(self, im):
        if isinstance(im, str):
            im = Image.open(im)
        frames = []

        try:
            for i in count(1):
                frames.append(ImageTk.PhotoImage(im.copy()))
                im.seek(i)
        except EOFError:
            pass
        self.frames = cycle(frames)

        try:
            self.delay = im.info['duration']
        except:
            self.delay = 100

        if len(frames) == 1:
            self.config(image=next(self.frames))
        else:
            self.next_frame()

    def unload(self):
        self.config(image=None)
        self.frames = None

    def next_frame(self):
        if self.frames:
            self.config(image=next(self.frames))
            self.after(self.delay, self.next_frame)
